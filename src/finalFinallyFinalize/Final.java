package finalFinallyFinalize;

public class Final {
	
	public static void main(String[] args) {
		
		final int x = 100;    // Value can't be updated  any more
		
		System.out.println(x);
		
   //		x =120;
		
		System.out.println("<----------- Method ----------->"); 
		System.out.println("final public float bankBalance()");
		System.out.println("OverRiding  - X");
		
		
		System.out.println("<----------- Class ----------->");
		System.out.println(" final class Bank ");
		System.out.println("Inheritance - X");
		
		System.out.println("<----------- Object ----------->");
		System.out.println("final user u = new user();");
		System.out.println("u = u2");
		System.out.println("Object Reference Cant be Changed");
		
		
		
		
		
	}

}
